    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int selectrow = tblUser.getSelectedRow();
        if (selectrow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        } else if (selectrow >= 0) {
            UserAccount u = (UserAccount) tblUser.getValueAt(selectrow, 0);
            Person p = (Person) tblUser.getValueAt(selectrow, 1);
            Enterprise e = (Enterprise) tblUser.getValueAt(selectrow, 3);

            txtPersonname.setText(p.getName());
            cbRole.removeAllItems();
            cbRole.addItem(String.valueOf(u.getRole()));
            txtUniversity.setText(e.getName());
            txtUserName.setText(u.getUsername());
            txtPass.setText(u.getPassword());
            txtPhone.setText(u.getPerson().getPhoneNumber());
            txtEmail.setText(u.getPerson().getEmail());
            lblPicture.setIcon(Util.FillLabel(u.getPerson().getPicturePath()));

        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectrow = tblUser.getSelectedRow();
        if (selectrow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        } else if (selectrow >= 0) {
            UserAccount u = (UserAccount) tblUser.getValueAt(selectrow, 0);
            Person p = (Person) tblUser.getValueAt(selectrow, 1);
            Enterprise e = (Enterprise) tblUser.getValueAt(selectrow, 3);

            for (Organization organization : e.getOrganizationDirectory().getOrganizationList()) {
                for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                    if (ua.getUsername().equals(u.getUsername())) {
                        organization.getUserAccountDirectory().getUserAccountList().remove(ua);
                        organization.getPersonDirectory().getPersonList().remove(p);
                        try {
                            try {
                                Util.SendSystem(system);
                            } catch (InterruptedException ex) {
                                Logger.getLogger(ManageUsersJPanel.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        } catch (IOException ex) {
                            Logger.getLogger(RegisterJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (ClassNotFoundException ex) {
                            Logger.getLogger(RegisterJFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }

                        JOptionPane.showMessageDialog(null, "Delete Successful");
                        populatetable();
                        break;
                    }
                }
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

