    private void populatetable() {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkDirectory().getNetworkList()) {
            for (State state : network.getStateDirectory().getStateList()) {
                for (City city : state.getCityDirectory().getCityList()) {
                    for (Enterprise enterprise1 : city.getEnterpriseDirectory().getEnterpriseList()) {
                        if (enterprise.equals(enterprise1)) {
                            for (Organization o : enterprise1.getOrganizationDirectory().getOrganizationList()) {
                                for (UserAccount u : o.getUserAccountDirectory().getUserAccountList()) {
                                    for (Person p : o.getPersonDirectory().getPersonList()) {

                                        Object[] row = new Object[5];
                                        row[0] = u;
                                        row[1] = p;
                                        row[2] = u.getRole();
                                        row[3] = enterprise1;
                                        row[4] = u.getStatus();
                                        model.addRow(row);
                                    }
                                }
                            }
                        }
                    }

                }

            }

        }
    }

