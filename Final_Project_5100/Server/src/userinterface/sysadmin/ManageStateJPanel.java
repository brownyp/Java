    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().length() < 1) {
            JOptionPane.showMessageDialog(null, "Please complete blanks", "Warning", WARNING_MESSAGE);
            return;
        }
        Network net1 = (Network) cbNetwork.getSelectedItem();
        for (State ss1 : net1.getStateDirectory().getStateList()) {
            if (ss1.getName().equals(txtName.getText())) {
                JOptionPane.showMessageDialog(null, "State existed!", "Warning", WARNING_MESSAGE);
                return;
            }
        }

        State s1 = new State();
        s1.setName(txtName.getText());
        Network network = (Network) cbNetwork.getSelectedItem();
        network.getStateDirectory().getStateList().add(s1);
        JOptionPane.showMessageDialog(null, "Submit Successfully!");
        populateTable();

    }//GEN-LAST:event_btnSubmitActionPerformed

