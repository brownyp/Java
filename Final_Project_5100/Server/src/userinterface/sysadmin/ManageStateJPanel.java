    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblState.getSelectedRow();
        Network network = (Network) cbNetwork.getSelectedItem();
        if (selectedrow >= 0) {
            State s1 = (State) tblState.getValueAt(selectedrow, 0);
            for (State state : network.getStateDirectory().getStateList()) {
                if (state.getName().equals(s1.getName())) {
                    network.getStateDirectory().getStateList().remove(state);
                }
            }
            JOptionPane.showMessageDialog(null, "Delete Successfully!");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

