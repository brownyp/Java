    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedrow = jTable1.getSelectedRow();
        if (selectedrow >= 0) {
            Book book = (Book) jTable1.getValueAt(selectedrow, 0);
            ArrayList<Book> bd = business.getBookDirectory().getBookDirectory();
            Order userOrder = new Order();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy年MM月dd日 HH:mm:ss");
            Date date = new Date();

            for (int i = 0; i < bd.size(); i++) {
                if (book.getBookName().equals(bd.get(i).getBookName())) {
                    bd.get(i).setStatus("已借出");
                    userOrder.setBook(bd.get(i));
                    userOrder.setUser(user);
                    userOrder.setAmountOfMoney(5);
                    userOrder.setDate(sdf.format(date));
                }
            }
            business.getOrderDirectory().getOrderDirectory().add(userOrder);
            user.getOrder().add(userOrder);

            JOptionPane.showMessageDialog(null, "借书成功，请等待出书");
        } else {
            JOptionPane.showMessageDialog(null, "请选择一本书");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

