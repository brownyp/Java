    public void populateTable() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (Book book : business.getBookDirectory().getBookDirectory()) {
            if (!book.getStatus().equals("已借出")) {
                Object row[] = new Object[2];
                row[0] = book.getId();
                row[1] = book.getBookName();
                dtm.addRow(row);
            }

        }
    }

